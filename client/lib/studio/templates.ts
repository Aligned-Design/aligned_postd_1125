/**
 * Starter Templates Library
 * 
 * A collection of 18 pre-designed templates for Creative Studio.
 * Templates are brand-adaptive and can be customized with brand colors and fonts.
 */

import type { Design, CanvasItem } from "@/types/creativeStudio";
import type { BrandGuide } from "@/types/brandGuide";

export type TemplateCategory = "quote" | "promo" | "testimonial" | "event" | "carousel" | "cover";
export type TemplateFormat = "square" | "story" | "portrait" | "landscape" | "email" | "flyer";

export interface StarterTemplate {
  id: string;
  name: string;
  category: TemplateCategory;
  format: TemplateFormat;
  description: string;
  previewImageUrl?: string; // For future use with actual preview images
  design: Design; // Full design structure
}

/**
 * Brand adaptation helper
 * Replaces placeholder colors with brand colors from brand kit
 */
function adaptTemplateToBrand(
  items: CanvasItem[],
  brandKit?: BrandGuide | null
): CanvasItem[] {
  if (!brandKit) return items;

  // âœ… FIX: Extract colors from colors array (visualIdentity doesn't have individual color properties)
  const colors = brandKit.visualIdentity?.colors || [];
  // âœ… FIX: Use legacy fields as fallback if available
  const primaryColor = colors[0] || (brandKit as any).primaryColor || "#8B5CF6";
  const secondaryColor = colors[1] || (brandKit as any).secondaryColor || "#F0F7F7";
  const accentColor = colors[2] || (brandKit as any).accentColor || "#EC4899";
  const textPrimary = (brandKit as any).textColor || "#1F2937"; // Default text color
  const textOnPrimary = (brandKit as any).textOnPrimary || "#FFFFFF"; // Default text on primary
  const fontFamily = brandKit.visualIdentity?.typography?.heading || (brandKit as any).fontFamily || "Arial";

  return items.map((item) => {
    const adapted = { ...item };

    // Adapt text colors and fonts
    if (item.type === "text") {
      // Replace placeholder colors with brand colors
      if (item.fontColor === "#8B5CF6" || item.fontColor === "#6366F1") {
        adapted.fontColor = primaryColor;
      } else if (item.fontColor === "#F0F7F7" || item.fontColor === "#F8FAFC") {
        adapted.fontColor = secondaryColor;
      } else if (item.fontColor === "#EC4899" || item.fontColor === "#F97316") {
        adapted.fontColor = accentColor;
      } else if (item.fontColor === "#FFFFFF" && item.backgroundColor === "#8B5CF6") {
        adapted.fontColor = textOnPrimary;
      } else if (!item.fontColor || item.fontColor === "#1F2937") {
        adapted.fontColor = textPrimary;
      }

      // Apply brand font if available
      if (fontFamily && fontFamily !== "Arial") {
        adapted.fontFamily = fontFamily;
      }
    }

    // Adapt shape colors
    if (item.type === "shape") {
      if (item.fill === "#8B5CF6" || item.fill === "#6366F1") {
        adapted.fill = primaryColor;
      } else if (item.fill === "#F0F7F7" || item.fill === "#F8FAFC") {
        adapted.fill = secondaryColor;
      } else if (item.fill === "#EC4899" || item.fill === "#F97316") {
        adapted.fill = accentColor;
      }
    }

    // Adapt background colors
    if (item.type === "background") {
      if (item.backgroundColor === "#8B5CF6" || item.backgroundColor === "#6366F1") {
        adapted.backgroundColor = primaryColor;
      } else if (item.backgroundColor === "#F0F7F7" || item.backgroundColor === "#F8FAFC") {
        adapted.backgroundColor = secondaryColor;
      }

      // Adapt gradient colors
      if (item.backgroundType === "gradient") {
        if (item.gradientFrom === "#8B5CF6" || item.gradientFrom === "#6366F1") {
          adapted.gradientFrom = primaryColor;
        }
        if (item.gradientTo === "#8B5CF6" || item.gradientTo === "#EC4899") {
          adapted.gradientTo = accentColor;
        }
        if (item.gradientFrom === "#F0F7F7") {
          adapted.gradientFrom = secondaryColor;
        }
      }
    }

    return adapted;
  });
}

/**
 * Create a template design with brand adaptation
 */
export function createTemplateDesign(
  template: StarterTemplate,
  brandId: string,
  brandKit?: BrandGuide | null
): Design {
  const adaptedItems = adaptTemplateToBrand(template.design.items, brandKit);
  
  return {
    ...template.design,
    id: `design-${Date.now()}`,
    brandId,
    items: adaptedItems,
    createdAt: new Date().toISOString(),
    updatedAt: new Date().toISOString(),
  };
}

